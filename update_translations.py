def update_translations_recursive(data, translations, lang_code):
    """Recursively update translations in the data structure."""
    if isinstance(data, dict):
        for key, value in data.items():
            if isinstance(value, dict):
                update_translations_recursive(value, translations, lang_code)
            elif isinstance(value, str) and value in translations.get(lang_code, {}):
                data[key] = translations[lang_code][value]
    return data

def update_translation_file(file_path, lang_code):
    """Update a single translation file."""
    # Load the current file
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Update translations
    updated_data = update_translations_recursive(data, TRANSLATIONS, lang_code)
    
    # Save the updated file
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(updated_data, f, ensure_ascii=False, indent=2)
    
    print(f"Updated {file_path}")

def main():
    base_dir = "/Users/mikail/Desktop/BDC/client/src/i18n/locales"
    
    # Update each language file
    for lang_code in ['tr', 'ar', 'es']:
        file_path = os.path.join(base_dir, f"{lang_code}.json")
        if os.path.exists(file_path):
            update_translation_file(file_path, lang_code)
        else:
            print(f"File not found: {file_path}")

if __name__ == "__main__":
    main()